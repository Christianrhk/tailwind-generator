                        <h4>Ile numerów liczy kolekcja?</h4>
                        <div>W ramach kolekcji planujemy wydać około 110 numerów.</div>
                        <h4>Jak często będą się ukazywały kolejne tomy?</h4>
                        <div>Kolejne tomy będą się ukazywały co 2 tygodnie w środę. Numer drugi (tom 3 + tom 4) ukaże
                            się 25.01.2017r.
                        </div>
                        <h4>Ile będzie kosztował jeden tom?</h4>
                        <div>Cena regularnego numeru, zawierającego jeden tom, dostępna w kiosku, będzie wynosiła 29,99
                            zł. W ofercie dla prenumeratorów jeden tom będzie kosztował 24,99zł.
                        </div>
                        <h4>Czy można kolekcję zaprenumerować od dowolnego numeru?</h4>
