                        <h4>Czy można zaprenumerować wybrane tomy?</h4>
                        <div>Niestety, nie. Można natomiast zamawiać wydane już numery, które zostaną wysłane za
                            zaliczeniem pocztowym. Oczywiście można zamawiać dowolną ilość numerów, a koszty wysyłki 10
                            zł doliczane są do paczki.
                        </div>
                        <h4>Czy można zaprenumerować kolekcję w formie przedpłaty?</h4>
